package p1;

/*Switch */
public class Ex9 {
	public static void main(String[] args) {
		int key = 1;
		switch (key) {
		case 1:
			System.out.println(1);
			break;
		case 2:
			System.out.println(2);
			break;
		case 3:
			System.out.println(3);
			break;
		case 4:
			System.out.println(4);
			break;
		default: // if no key matches. 
			break;
		}
	}
}
